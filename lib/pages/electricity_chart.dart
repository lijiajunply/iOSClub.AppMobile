class ElectricityChart extends StatelessWidget {
  const ElectricityChart({super.key});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('电费数据'),
      ),
      body: FutureBuilder(
        future: OtherService.getElectricityWeeklyData(),
        builder: (context, snapshot) {
          if (snapshot.connectionState == ConnectionState.waiting) {
            return Center(child: CircularProgressIndicator());
          }
          if (snapshot.hasError) {
            return Center(child: Text('加载失败: ${snapshot.error}'));
          }
          return Padding(
              padding: EdgeInsets.all(16),
              child: AspectRatio(
                aspectRatio: 1.6,
                child: AppBarChart(
                  data: snapshot.data!,
                ),
              ));
        },
      ),
    );
  }
}

class AppBarChart extends StatelessWidget {
  final List<ElectricData> data;

  const AppBarChart({super.key, required this.data});

  @override
  Widget build(BuildContext context) {
    double dataMaxValue = data.map((e) => e.value).reduce(max);
    final calculatedMaxY = dataMaxValue.ceilToDouble() * 1.2;
    return BarChart(
      BarChartData(
        barTouchData: barTouchData,
        titlesData: titlesData,
        borderData: borderData,
        barGroups: data
            .asMap()
            .map((x, y) {
              return MapEntry(
                  x,
                  BarChartGroupData(
                    x: x,
                    barRods: [
                      BarChartRodData(
                        toY: y.value,
                        gradient: _barsGradient,
                      ),
                    ],
                    showingTooltipIndicators: [0],
                  ));
            })
            .values
            .toList(),
        gridData: const FlGridData(show: false),
        alignment: BarChartAlignment.spaceAround,
        maxY: calculatedMaxY,
      ),
    );
  }

  BarTouchData get barTouchData => BarTouchData(
        enabled: false,
        touchTooltipData: BarTouchTooltipData(
          getTooltipColor: (group) => Colors.transparent,
          tooltipPadding: EdgeInsets.zero,
          tooltipMargin: 8,
          getTooltipItem: (
            BarChartGroupData group,
            int groupIndex,
            BarChartRodData rod,
            int rodIndex,
          ) {
            return BarTooltipItem(
              rod.toY.toStringAsFixed(1),
              const TextStyle(
                color: AppColors.contentColorCyan,
                fontWeight: FontWeight.bold,
              ),
            );
          },
        ),
      );

  Widget getTitles(double value, TitleMeta meta) {
    final style = TextStyle(
      color: AppColors.contentColorBlue.darken(20),
      fontWeight: FontWeight.bold,
      fontSize: 14,
    );
    var a = data[value.toInt()].timestamp.hour;
    return SideTitleWidget(
      meta: meta,
      space: 4,
      child: Text('$a', style: style),
    );
  }

  FlTitlesData get titlesData => FlTitlesData(
        show: true,
        bottomTitles: AxisTitles(
          sideTitles: SideTitles(
            showTitles: true,
            reservedSize: 30,
            getTitlesWidget: getTitles,
          ),
        ),
        leftTitles: const AxisTitles(
          sideTitles: SideTitles(showTitles: false),
        ),
        topTitles: const AxisTitles(
          sideTitles: SideTitles(showTitles: false),
        ),
        rightTitles: const AxisTitles(
          sideTitles: SideTitles(showTitles: false),
        ),
      );

  FlBorderData get borderData => FlBorderData(
        show: false,
      );

  LinearGradient get _barsGradient => LinearGradient(
        colors: [
          AppColors.contentColorBlue.darken(20),
          AppColors.contentColorCyan,
        ],
        begin: Alignment.bottomCenter,
        end: Alignment.topCenter,
      );
}

